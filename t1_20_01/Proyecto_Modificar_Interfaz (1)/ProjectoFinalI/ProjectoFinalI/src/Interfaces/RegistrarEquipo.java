    private void jButton_RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegistrarActionPerformed

        int validacion = 0;
        String ipequipo, tipo_equipo, puerto, host, servidor, dia_ingreso, mes_ingreso, annio_ingreso,
        estatus, observaciones, dns;

        ipequipo = txt_IpEquipo.getText().trim();
        tipo_equipo = cmb_tipoequipo.getSelectedItem().toString();
        puerto = cmb_puerto.getSelectedItem().toString();
        host = txt_host.getText().trim();
        servidor = txt_servidor.getText().trim();
        observaciones = jTextPane_observaciones.getText();
        dns = txt_dns.getText().trim();
        estatus = "Nuevo ingreso";

        Calendar calendar = Calendar.getInstance();

        dia_ingreso = Integer.toString(calendar.get(Calendar.DATE));
        mes_ingreso = Integer.toString(calendar.get(Calendar.MONTH));
        annio_ingreso = Integer.toString(calendar.get(Calendar.YEAR));

        if (ipequipo.equals("")) {
            txt_IpEquipo.setBackground(Color.red);
            validacion++;
        }
        if (host.equals("")) {
            txt_host.setBackground(Color.red);
            validacion++;
        }
        if (servidor.equals("")) {
            txt_servidor.setBackground(Color.red);
            validacion++;
        }
        if (servidor.equals("")) {
            txt_dns.setBackground(Color.red);
            validacion++;
        }
        if (observaciones.equals("")) {
            jTextPane_observaciones.setText("Sin observaciones.");
        }

        if (validacion == 0) {

            try {
                Equipos.insertarFinE(new Dispositivos(tipo_equipo, ipequipo, host, servidor, puerto, dns));
                txt_IpEquipo.setBackground(Color.green);
                txt_host.setBackground(Color.green);
                txt_servidor.setBackground(Color.green);
                txt_dns.setBackground(Color.green);
                JOptionPane.showMessageDialog(null, "Registro exitoso.");
                this.dispose();

            } catch (Exception e) {
                System.err.println("Error en registrar equipo. " + e);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Debes de llenar todos los campos.");
        }
    }//GEN-LAST:event_jButton_RegistrarActionPerformed

